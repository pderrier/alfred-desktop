/// A single column mapping with an optional regex extraction pattern.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ColumnSpec {
    pub index: i32,
    #[serde(default)]
    pub parse_pattern: Option<String>,
}

/// Declarative spec describing how to parse any CSV format.
/// Generated by the LLM, executed deterministically by Rust.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct CsvParsingSpec {
    pub format_type: String,                                // "transaction_history" | "position_snapshot"
    pub delimiter: String,                                  // "," | ";" | "\t"
    #[serde(default)]
    pub header_row_index: usize,
    #[serde(default)]
    pub skip_rows_before_header: usize,
    pub number_format: String,                              // "english" | "french"
    pub columns: HashMap<String, Option<ColumnSpec>>,       // null = column absent
    #[serde(default)]
    pub action_map: Option<HashMap<String, Vec<String>>>,
    #[serde(default)]
    pub infer_action_from_quantity_sign: bool,
    #[serde(default = "default_confidence")]
    pub confidence: String,
}

fn default_confidence() -> String { "medium".to_string() }

/// Compute a stable fingerprint for a set of CSV headers.
/// Normalizes, sorts, joins, and hashes to produce a 16-char hex key.
fn compute_header_fingerprint(headers: &[String]) -> String {
    let mut normalized: Vec<String> = headers
        .iter()
        .map(|h| normalize_header(h))
        .collect();
    normalized.sort();
    let joined = normalized.join("|");
    // Simple hash — no crypto dependency needed for a cache key.
    // Using FNV-1a 64-bit for speed and reasonable distribution.
    let mut hash: u64 = 0xcbf29ce484222325;
    for byte in joined.as_bytes() {
        hash ^= *byte as u64;
        hash = hash.wrapping_mul(0x100000001b3);
    }
    format!("{:016x}", hash)
}

/// Look up a cached CsvParsingSpec by header fingerprint.
fn lookup_cached_spec(fingerprint: &str) -> Option<CsvParsingSpec> {
    let prefs = crate::runtime_settings::get_user_preferences();
    let entry = prefs.get("csv_parsing_specs")?.get(fingerprint)?;
    let spec_value = entry.get("spec")?;
    serde_json::from_value(spec_value.clone()).ok()
}

/// Cache a CsvParsingSpec keyed by header fingerprint.
fn cache_spec(fingerprint: &str, spec: &CsvParsingSpec, headers: &[String]) {
    let mut prefs = crate::runtime_settings::get_user_preferences();
    let specs = prefs
        .as_object_mut()
        .and_then(|obj| {
            obj.entry("csv_parsing_specs")
                .or_insert_with(|| json!({}))
                .as_object_mut()
                .map(|m| m as *mut Map<String, Value>)
        });
    if let Some(specs_ptr) = specs {
        let specs_obj = unsafe { &mut *specs_ptr };
        let hit_count = specs_obj
            .get(fingerprint)
            .and_then(|e| e.get("hit_count"))
            .and_then(|v| v.as_u64())
            .unwrap_or(0);
        specs_obj.insert(fingerprint.to_string(), json!({
            "spec": serde_json::to_value(spec).unwrap_or_default(),
            "cached_at": crate::now_iso_string(),
            "hit_count": hit_count + 1,
            "original_headers": headers,
        }));
    }
    if let Err(e) = crate::runtime_settings::save_user_preferences(&prefs) {
        crate::debug_log(&format!("[csv-cache] failed to save spec: {e}"));
    }
}

/// Increment hit count for a cached spec.
fn touch_cached_spec(fingerprint: &str) {
    let mut prefs = crate::runtime_settings::get_user_preferences();
    if let Some(entry) = prefs.get_mut("csv_parsing_specs")
        .and_then(|v| v.get_mut(fingerprint))
    {
        let count = entry.get("hit_count").and_then(|v| v.as_u64()).unwrap_or(0);
        if let Some(obj) = entry.as_object_mut() {
            obj.insert("hit_count".to_string(), json!(count + 1));
        }
        if let Err(e) = crate::runtime_settings::save_user_preferences(&prefs) {
            crate::debug_log(&format!("[csv-cache] failed to touch spec: {e}"));
        }
    }
}

/// Remove a cached spec (e.g. when it produced 0 positions).
fn invalidate_cached_spec(fingerprint: &str) {
    let mut prefs = crate::runtime_settings::get_user_preferences();
    if let Some(specs) = prefs.get_mut("csv_parsing_specs").and_then(|v| v.as_object_mut()) {
        specs.remove(fingerprint);
        if let Err(e) = crate::runtime_settings::save_user_preferences(&prefs) {
            crate::debug_log(&format!("[csv-cache] failed to invalidate spec: {e}"));
        }
    }
}

/// Pre-compile all parse_pattern regexes from a spec into a lookup map.
fn compile_spec_patterns(spec: &CsvParsingSpec) -> HashMap<String, Regex> {
    let mut patterns = HashMap::new();
    for (field, col_opt) in &spec.columns {
        if let Some(col) = col_opt {
            if let Some(pattern) = &col.parse_pattern {
                match Regex::new(pattern) {
                    Ok(re) => { patterns.insert(field.clone(), re); }
                    Err(e) => {
                        crate::debug_log(&format!(
                            "[csv-spec] invalid regex for field '{field}': {pattern} — {e}"
                        ));
                    }
                }
            }
        }
    }
    patterns
}

/// Read a cell value from a row, applying parse_pattern extraction if available.
fn read_cell(row: &[String], col: &ColumnSpec, field: &str, patterns: &HashMap<String, Regex>) -> String {
    if col.index < 0 { return String::new(); }
    let raw = row.get(col.index as usize).map(String::as_str).unwrap_or_default().trim();
    extract_with_pattern(raw, patterns.get(field))
}

/// Read a cell and parse it as a number using the spec's number format.
fn read_num(row: &[String], col: &ColumnSpec, field: &str, patterns: &HashMap<String, Regex>, number_format: &str) -> f64 {
    let extracted = read_cell(row, col, field, patterns);
    if extracted.is_empty() { return 0.0; }
    parse_number_with_format(&extracted, number_format)
}

/// Resolve action from raw value using action_map + infer_action_from_quantity_sign.
fn resolve_action(raw: &str, quantity: f64, spec: &CsvParsingSpec) -> String {
    if spec.infer_action_from_quantity_sign {
        return if quantity >= 0.0 { "BUY".to_string() } else { "SELL".to_string() };
    }
    if let Some(action_map) = &spec.action_map {
        let lower = raw.trim().to_lowercase();
        for (canonical, synonyms) in action_map {
            if synonyms.iter().any(|s| s.to_lowercase() == lower) {
                return canonical.to_uppercase();
            }
        }
    }
    // Fall back to existing normalize_action
    normalize_action(raw)
}

/// Execute a CsvParsingSpec against parsed rows to produce a portfolio snapshot.
/// This is the core deterministic executor — no LLM calls.
fn execute_spec(spec: &CsvParsingSpec, rows: &[Vec<String>], _headers: &[String], account: &str) -> Result<Value> {
    let patterns = compile_spec_patterns(spec);
    let nf = &spec.number_format;

    // Helper to get a column spec for a field
    let col_for = |field: &str| -> Option<&ColumnSpec> {
        spec.columns.get(field).and_then(|opt| opt.as_ref())
    };

    match spec.format_type.as_str() {
        "transaction_history" => {
            // Build a TransactionColumnMapping and delegate to parse_transactions + reconcile_transactions
            let txn_mapping = TransactionColumnMapping {
                date:     col_for("date").map(|c| c.index).unwrap_or(-1),
                ticker:   col_for("ticker").map(|c| c.index).unwrap_or(-1),
                isin:     col_for("isin").map(|c| c.index).unwrap_or(-1),
                name:     col_for("name").map(|c| c.index).unwrap_or(-1),
                action:   col_for("action").map(|c| c.index).unwrap_or(-1),
                quantity: col_for("quantity").map(|c| c.index).unwrap_or(-1),
                price:    col_for("price").map(|c| c.index).unwrap_or(-1),
                amount:   col_for("amount").map(|c| c.index).unwrap_or(-1),
                currency: col_for("currency").map(|c| c.index).unwrap_or(-1),
                fees:     col_for("fees").map(|c| c.index).unwrap_or(-1),
                fx_rate:  col_for("fx_rate").map(|c| c.index).unwrap_or(-1),
            };

            // Apply parse_patterns to pre-process rows before passing to parse_transactions
            let processed_rows: Vec<Vec<String>> = rows.iter().map(|row| {
                let mut new_row = row.clone();
                for (field, col_opt) in &spec.columns {
                    if let Some(col) = col_opt {
                        let idx = col.index as usize;
                        if idx < new_row.len() && patterns.contains_key(field) {
                            let extracted = read_cell(row, col, field, &patterns);
                            // For numeric fields with non-english format, convert to a parseable form
                            if nf == "english" && ["quantity", "price", "amount", "fees", "fx_rate"].contains(&field.as_str()) {
                                // Strip thousands commas so parse_fr_number (used inside parse_transactions) works
                                new_row[idx] = extracted.replace(',', "");
                            } else {
                                new_row[idx] = extracted;
                            }
                        }
                    }
                }
                new_row
            }).collect();

            // For DEGIRO-style: override action column with inferred action if needed
            let broker = if spec.infer_action_from_quantity_sign { "DEGIRO-style" } else { "LLM-spec" };
            let transactions = parse_transactions_with_spec(&processed_rows, &txn_mapping, broker, spec);
            if transactions.is_empty() {
                return Err(anyhow!("csv_spec_no_transactions_parsed"));
            }
            let snapshot = reconcile_transactions(&transactions, account);
            let pos_count = snapshot.get("positions").and_then(|v| v.as_array()).map(|a| a.len()).unwrap_or(0);
            if pos_count == 0 {
                return Err(anyhow!("csv_spec_no_open_positions"));
            }
            Ok(snapshot)
        }
        "position_snapshot" => {
            // Build a ColumnMapping and delegate to apply_column_mapping, but with pattern extraction
            let mut positions = Vec::new();
            for row in rows {
                let ticker_raw = col_for("ticker")
                    .map(|c| read_cell(row, c, "ticker", &patterns))
                    .unwrap_or_default()
                    .trim().to_uppercase();
                let isin_raw = col_for("isin")
                    .map(|c| read_cell(row, c, "isin", &patterns))
                    .unwrap_or_default()
                    .trim().to_uppercase();
                let name_raw = col_for("name")
                    .map(|c| read_cell(row, c, "name", &patterns))
                    .unwrap_or_default();

                // Must have at least ticker or ISIN
                let ticker = if !ticker_raw.is_empty() {
                    ticker_raw.clone()
                } else if !isin_raw.is_empty() {
                    isin_raw.clone()
                } else if !name_raw.is_empty() {
                    derive_ticker_from_name(&name_raw, "")
                } else {
                    continue;
                };

                let quantity = col_for("quantity")
                    .map(|c| read_num(row, c, "quantity", &patterns, nf))
                    .unwrap_or(0.0);
                if quantity == 0.0 || !quantity.is_finite() { continue; }

                let prix_actuel = col_for("current_price")
                    .or(col_for("price"))
                    .map(|c| read_num(row, c, "current_price", &patterns, nf))
                    .unwrap_or(0.0);
                let market_value = col_for("market_value")
                    .map(|c| read_num(row, c, "market_value", &patterns, nf))
                    .unwrap_or(0.0);
                let cost_basis = col_for("cost_basis")
                    .map(|c| read_num(row, c, "cost_basis", &patterns, nf))
                    .unwrap_or(0.0);
                let pnl = col_for("pnl")
                    .map(|c| read_num(row, c, "pnl", &patterns, nf))
                    .unwrap_or(0.0);
                let compte = col_for("account")
                    .map(|c| read_cell(row, c, "account", &patterns))
                    .unwrap_or_default();

                let valeur_actuelle = if market_value != 0.0 { market_value } else { quantity * prix_actuel };
                let prix_revient = if cost_basis != 0.0 {
                    cost_basis
                } else if valeur_actuelle != 0.0 && pnl != 0.0 {
                    (valeur_actuelle - pnl) / quantity
                } else {
                    0.0
                };

                positions.push(
                    serde_json::to_value(crate::models::Position {
                        ticker: ticker.clone(),
                        nom: if name_raw.is_empty() { ticker.clone() } else { name_raw },
                        isin: if isin_raw.is_empty() { None } else { Some(isin_raw) },
                        quantite: quantity,
                        prix_actuel,
                        valeur_actuelle,
                        prix_revient,
                        plus_moins_value: pnl,
                        plus_moins_value_pct: 0.0,
                        compte: if compte.is_empty() { account.to_string() } else { compte },
                        position_type: None,
                    }).unwrap_or_default(),
                );
            }
            if positions.is_empty() {
                return Err(anyhow!("csv_spec_no_positions_found"));
            }
            Ok(json!({
                "positions": positions,
                "valeur_totale": positions.iter().filter_map(|p| p.get("valeur_actuelle").and_then(|v| v.as_f64())).sum::<f64>(),
                "plus_value_totale": positions.iter().filter_map(|p| p.get("plus_moins_value").and_then(|v| v.as_f64())).sum::<f64>(),
                "liquidites": 0.0
            }))
        }
        other => Err(anyhow!("csv_spec_unknown_format_type:{other}"))
    }
}

/// Parse transactions using a spec-aware approach (respects action_map and infer_action_from_quantity_sign).
fn parse_transactions_with_spec(
    rows: &[Vec<String>],
    mapping: &TransactionColumnMapping,
    broker_name: &str,
    spec: &CsvParsingSpec,
) -> Vec<CsvTransaction> {
    let col = |row: &Vec<String>, idx: i32| -> String {
        if idx < 0 { return String::new(); }
        row.get(idx as usize).cloned().unwrap_or_default().trim().to_string()
    };
    let num = |row: &Vec<String>, idx: i32| -> f64 {
        if idx < 0 { return 0.0; }
        parse_fr_number(row.get(idx as usize).map(String::as_str).unwrap_or_default())
    };

    let mut transactions = Vec::new();
    for row in rows {
        let raw_action = col(row, mapping.action);
        let quantity_raw = num(row, mapping.quantity);
        let action = resolve_action(&raw_action, quantity_raw, spec);

        if action != "BUY" && action != "SELL" {
            continue;
        }

        let quantity = quantity_raw.abs();
        if quantity == 0.0 || !quantity.is_finite() {
            continue;
        }

        let price = num(row, mapping.price).abs();
        let amount_raw = num(row, mapping.amount);
        let amount = if amount_raw != 0.0 { amount_raw.abs() } else { quantity * price };

        let ticker_raw = col(row, mapping.ticker).to_uppercase();
        let isin_raw = col(row, mapping.isin).to_uppercase();
        let name_raw = col(row, mapping.name);

        if ticker_raw.is_empty() && isin_raw.is_empty() && name_raw.is_empty() {
            continue;
        }

        let ticker = if !ticker_raw.is_empty() {
            ticker_raw.clone()
        } else if !isin_raw.is_empty() {
            isin_raw.clone()
        } else {
            derive_ticker_from_name(&name_raw, "")
        };

        let currency = col(row, mapping.currency).to_uppercase();
        let fx_rate_raw = num(row, mapping.fx_rate);
        let fx_rate = if fx_rate_raw > 0.0 && fx_rate_raw.is_finite() { fx_rate_raw } else { 1.0 };
        let fees = num(row, mapping.fees).abs();
        let date = normalize_date(&col(row, mapping.date));

        transactions.push(CsvTransaction {
            date,
            ticker,
            isin: isin_raw,
            name: if name_raw.is_empty() { ticker_raw.clone() } else { name_raw },
            action,
            quantity,
            price,
            amount,
            currency: if currency.is_empty() { "EUR".to_string() } else { currency },
            fees,
            fx_rate,
        });
    }

    crate::debug_log(&format!(
        "[csv-spec] parsed {} transactions from {} rows (broker: {})",
        transactions.len(), rows.len(), broker_name
    ));
    transactions
}

fn strip_csv_quotes(raw: &str) -> String {
    let trimmed = raw.trim_start_matches('\u{FEFF}').trim();
    if trimmed.starts_with('"') && trimmed.ends_with('"') && trimmed.len() >= 2 {
        return trimmed[1..trimmed.len() - 1].replace("\"\"", "\"").trim().to_string();
    }
    trimmed.to_string()
}

fn split_semicolon(line: &str) -> Vec<String> {
    line.split(';').map(strip_csv_quotes).collect()
}

fn normalize_header(raw: &str) -> String {
    strip_csv_quotes(raw)
        .to_lowercase()
        .replace(['à', 'â', 'ä'], "a")
        .replace(['é', 'è', 'ê', 'ë'], "e")
        .replace(['î', 'ï'], "i")
        .replace(['ô', 'ö'], "o")
        .replace(['ù', 'û', 'ü'], "u")
        .replace('ç', "c")
        .replace(['’', '\''], "'")
        .trim()
        .to_string()
}

fn resolve_latest_by_prefix(base_dir: &PathBuf, prefix: &str) -> Result<Option<PathBuf>> {
    let mut entries = fs::read_dir(base_dir)?
        .filter_map(|entry| entry.ok())
        .map(|entry| entry.path())
        .filter(|path| {
            path.file_name()
                .and_then(|name| name.to_str())
                .map(|name| name.starts_with(prefix) && name.ends_with(".csv"))
                .unwrap_or(false)
        })
        .collect::<Vec<_>>();
    entries.sort_by(|left, right| {
        let left_mtime = fs::metadata(left)
            .and_then(|metadata| metadata.modified())
            .ok();
        let right_mtime = fs::metadata(right)
            .and_then(|metadata| metadata.modified())
            .ok();
        right_mtime.cmp(&left_mtime)
    });
    Ok(entries.into_iter().next())
}

fn resolve_export_files(export_path: &str) -> Result<(PathBuf, Option<PathBuf>, Option<PathBuf>)> {
    let candidate = PathBuf::from(export_path);
    let metadata = fs::metadata(&candidate)
        .map_err(|error| anyhow!("export_path_not_found:{export_path}:{error}"))?;
    let base_dir = if metadata.is_dir() {
        candidate
    } else {
        candidate
            .parent()
            .map(PathBuf::from)
            .ok_or_else(|| anyhow!("export_path_not_found:{export_path}"))?
    };
    let positions = resolve_latest_by_prefix(&base_dir, "POSITIONS_")?
        .ok_or_else(|| anyhow!("positions_export_not_found:{export_path}"))?;
    let history = resolve_latest_by_prefix(&base_dir, "HISTORY_")?;
    let orders = resolve_latest_by_prefix(&base_dir, "ORDERS_")?;
    Ok((positions, history, orders))
}

fn parse_positions_file(path: &PathBuf, account_name: &str) -> Result<Value> {
    let text = fs::read_to_string(path)?;
    parse_positions_text(&text, account_name)
}

/// Parse CSV positions text (Boursorama semicolon format) into a portfolio snapshot.
fn parse_positions_text(text: &str, account_name: &str) -> Result<Value> {
    let lines = text.lines().filter(|line| !line.trim().is_empty()).collect::<Vec<_>>();
    let totals_line = lines
        .iter()
        .find(|line| line.contains("Valo total") && line.contains("Solde espèces"))
        .copied()
        .unwrap_or_default();
    let valeur_totale = totals_line
        .split("Valo total")
        .nth(1)
        .and_then(|part| part.split('=').nth(1))
        .and_then(|part| part.split(';').next())
        .map(parse_fr_number)
        .unwrap_or(0.0);
    let plus_value_totale = totals_line
        .split("+/- value latente")
        .nth(1)
        .and_then(|part| part.split('=').nth(1))
        .and_then(|part| part.split(';').next())
        .map(parse_fr_number)
        .unwrap_or(0.0);
    let liquidites = totals_line
        .split("Solde espèces")
        .nth(1)
        .and_then(|part| part.split('=').nth(1))
        .and_then(|part| part.split(';').next())
        .map(parse_fr_number)
        .unwrap_or(0.0);
    let header_index = lines.iter().position(|line| {
        let cols = split_semicolon(line);
        cols.first().map(|value| normalize_header(value)).unwrap_or_default() == "nom"
            && cols.get(1)
                .map(|value| {
                    let normalized = normalize_header(value);
                    normalized == "code isin" || normalized == "isin"
                })
                .unwrap_or(false)
    });
    let mut positions = Vec::new();
    if let Some(index) = header_index {
        for line in lines.iter().skip(index + 1) {
            let cols = split_semicolon(line);
            if cols.len() < 11 {
                continue;
            }
            let nom = cols[0].clone();
            let isin = cols[1].clone();
            let ticker = derive_ticker_from_name(&nom, &isin);
            positions.push(serde_json::to_value(crate::models::Position {
                ticker,
                nom,
                isin: if isin.trim().is_empty() { None } else { Some(isin) },
                quantite: parse_fr_number(&cols[2]),
                prix_actuel: parse_fr_number(&cols[3]),
                valeur_actuelle: parse_fr_number(&cols[4]),
                prix_revient: parse_fr_number(&cols[6]),
                plus_moins_value: parse_fr_number(&cols[8]),
                plus_moins_value_pct: parse_fr_number(&cols[10]),
                compte: account_name.to_string(),
                position_type: None,
            }).unwrap_or_default());
        }
    }
    Ok(json!({
        "positions": positions,
        "valeur_totale": valeur_totale,
        "plus_value_totale": plus_value_totale,
        "liquidites": liquidites
    }))
}

fn parse_history_file(path: Option<PathBuf>) -> Result<Value> {
    let Some(path) = path else { return Ok(json!([])); };
    let text = fs::read_to_string(path)?;
    let lines = text.lines().filter(|line| !line.trim().is_empty()).collect::<Vec<_>>();
    let header_index = lines
        .iter()
        .position(|line| normalize_header(split_semicolon(line).first().map(String::as_str).unwrap_or_default()) == "operation");
    let Some(index) = header_index else { return Ok(json!([])); };
    let mut out = Vec::new();
    for line in lines.iter().skip(index + 1) {
        let cols = split_semicolon(line);
        if cols.len() < 7 {
            continue;
        }
        out.push(json!({
            "operation": cols[0],
            "value": cols[1],
            "isin": cols[2],
            "quantity": parse_fr_number(&cols[3]),
            "amount": parse_fr_number(&cols[4]),
            "fees": parse_fr_number(&cols[5]),
            "date": cols[6]
        }));
    }
    Ok(Value::Array(out))
}

fn parse_orders_file(path: Option<PathBuf>) -> Result<Value> {
    let Some(path) = path else { return Ok(json!([])); };
    let text = fs::read_to_string(path)?;
    let lines = text.lines().filter(|line| !line.trim().is_empty()).collect::<Vec<_>>();
    let header_index = lines.iter().position(|line| {
        let first = normalize_header(split_semicolon(line).first().map(String::as_str).unwrap_or_default());
        first == "date de mise a jour" || first == "date"
    });
    let Some(index) = header_index else { return Ok(json!([])); };
    if lines.len() < index + 2 {
        return Ok(json!([]));
    }
    let headers = split_semicolon(lines[index]);
    let mut out = Vec::new();
    for line in lines.iter().skip(index + 1) {
        let cols = split_semicolon(line);
        if cols.len() < 2 {
            continue;
        }
        let mut row = Map::new();
        for (idx, header) in headers.iter().enumerate() {
            row.insert(header.clone(), Value::String(cols.get(idx).cloned().unwrap_or_default()));
        }
        out.push(Value::Object(row));
    }
    Ok(Value::Array(out))
}

/// Prepare CSV text for parsing: strip file separators and Boursorama metadata.
/// Returns (cleaned_text, boursorama_metadata_if_any).
fn prepare_csv_text(raw: &str) -> (String, Option<Value>) {
    let text: String = raw
        .lines()
        .filter(|line| !line.starts_with("--- FILE: "))
        .collect::<Vec<_>>()
        .join("\n");

    // Extract Boursorama metadata (Valo total line) before removing it
    let metadata = text.lines()
        .find(|line| line.contains("Valo total") && line.contains("Solde espèces"))
        .map(|totals_line| {
            let valeur_totale = totals_line.split("Valo total").nth(1)
                .and_then(|p| p.split('=').nth(1))
                .and_then(|p| p.split(';').next())
                .map(parse_fr_number).unwrap_or(0.0);
            let plus_value_totale = totals_line.split("+/- value latente").nth(1)
                .and_then(|p| p.split('=').nth(1))
                .and_then(|p| p.split(';').next())
                .map(parse_fr_number).unwrap_or(0.0);
            let liquidites = totals_line.split("Solde espèces").nth(1)
                .and_then(|p| p.split('=').nth(1))
                .and_then(|p| p.split(';').next())
                .map(parse_fr_number).unwrap_or(0.0);
            json!({ "valeur_totale": valeur_totale, "plus_value_totale": plus_value_totale, "liquidites": liquidites })
        });

    (text, metadata)
}

/// Parse raw CSV text from the file picker (possibly multiple files concatenated
/// with `--- FILE: name.csv ---` separators). Returns a portfolio snapshot.
///
/// Two-tier parsing strategy:
/// 1. **Cache lookup** — header fingerprint → stored CsvParsingSpec → execute_spec()
/// 2. **LLM analysis** — LLM generates CsvParsingSpec → execute_spec() → cache on success
///
/// Boursorama metadata (Valo total line) is extracted before parsing if present.
fn parse_csv_upload_text(raw: &str, account: &str) -> Result<Value> {
    let (text, boursorama_meta) = prepare_csv_text(raw);
    let acct = if account.is_empty() { "CSV_import" } else { account };

    // ── Boursorama special case (semicolon metadata format) ─────────
    if boursorama_meta.is_some() {
        if let Ok(snap) = parse_positions_text(&text, acct) {
            let pos = snap.get("positions").and_then(|v| v.as_array());
            if pos.map(|a| !a.is_empty()).unwrap_or(false) {
                crate::debug_log("[csv-parse] Boursorama metadata format detected");
                return wrap_snapshot(snap);
            }
        }
    }

    // ── Parse headers and data rows ─────────────────────────────────
    let delimiter = detect_csv_delimiter(&text);
    let lines: Vec<&str> = text.lines().filter(|l| !l.trim().is_empty()).collect();
    if lines.len() < 2 {
        return Err(anyhow!("csv_upload_no_positions_found"));
    }
    let headers: Vec<String> = split_csv_line(lines[0], delimiter);
    let data_rows: Vec<Vec<String>> = lines[1..]
        .iter()
        .map(|line| split_csv_line(line, delimiter))
        .collect();
    let fingerprint = compute_header_fingerprint(&headers);

    // ── Tier 1: Cache lookup ────────────────────────────────────────
    if let Some(cached_spec) = lookup_cached_spec(&fingerprint) {
        crate::debug_log(&format!("[csv-parse] tier 1: cache hit for fingerprint {fingerprint}"));
        match execute_spec(&cached_spec, &data_rows, &headers, acct) {
            Ok(snapshot) => {
                let pos_count = snapshot.get("positions").and_then(|v| v.as_array()).map(|a| a.len()).unwrap_or(0);
                if pos_count > 0 {
                    touch_cached_spec(&fingerprint);
                    return wrap_snapshot(snapshot);
                }
                crate::debug_log("[csv-parse] cached spec produced 0 positions, invalidating cache");
                invalidate_cached_spec(&fingerprint);
            }
            Err(e) => {
                crate::debug_log(&format!("[csv-parse] cached spec execution failed: {e}, invalidating"));
                invalidate_cached_spec(&fingerprint);
            }
        }
    }

    // ── Tier 2: LLM analysis ────────────────────────────────────────
    crate::debug_log("[csv-parse] tier 2: requesting LLM CSV analysis");
    let sample: Vec<Vec<String>> = data_rows.iter().take(5).cloned().collect();
    let spec = crate::llm::analyze_csv_format(&headers, &sample, delimiter, data_rows.len())?;
    crate::debug_log(&format!("[csv-parse] LLM spec: format={}, confidence={}", spec.format_type, spec.confidence));

    match execute_spec(&spec, &data_rows, &headers, acct) {
        Ok(snapshot) => {
            let pos_count = snapshot.get("positions").and_then(|v| v.as_array()).map(|a| a.len()).unwrap_or(0);
            if pos_count > 0 {
                cache_spec(&fingerprint, &spec, &headers);
                crate::debug_log(&format!("[csv-parse] cached spec for fingerprint {fingerprint} ({pos_count} positions)"));
                return wrap_snapshot(snapshot);
            }
            Err(anyhow!("csv_upload_no_positions_found"))
        }
        Err(e) => Err(anyhow!("csv_upload_spec_failed:{e}"))
    }
}

fn wrap_snapshot(portfolio: Value) -> Result<Value> {
    if portfolio.get("positions").and_then(|v| v.as_array()).map(|a| a.is_empty()).unwrap_or(true) {
        return Err(anyhow!("csv_upload_no_positions_found"));
    }
    let mut result = json!({
        "portfolio_source": "csv",
        "positions": portfolio.get("positions").cloned().unwrap_or_else(|| json!([])),
        "transactions": [],
        "orders": [],
        "valeur_totale": portfolio.get("valeur_totale").cloned().unwrap_or_else(|| json!(0.0)),
        "plus_value_totale": portfolio.get("plus_value_totale").cloned().unwrap_or_else(|| json!(0.0)),
        "liquidites": portfolio.get("liquidites").cloned().unwrap_or_else(|| json!(0.0))
    });
    // Preserve transaction history metadata (source, reconciliation) through normalization
    if let Some(source) = portfolio.get("source") {
        if let Some(obj) = result.as_object_mut() {
            obj.insert("csv_source".to_string(), source.clone());
        }
    }
    if let Some(reconciliation) = portfolio.get("reconciliation") {
        if let Some(obj) = result.as_object_mut() {
            obj.insert("reconciliation".to_string(), reconciliation.clone());
        }
    }
    Ok(result)
}

// ── CSV parsing utilities ───────────────────────────────────────

fn detect_csv_delimiter(text: &str) -> char {
    let first_line = text.lines().next().unwrap_or_default();
    let candidates = [';', ',', '\t', '|'];
    candidates
        .iter()
        .copied()
        .max_by_key(|&d| first_line.matches(d).count())
        .unwrap_or(',')
}

fn split_csv_line(line: &str, delimiter: char) -> Vec<String> {
    // Handle quoted fields (e.g. "some, value")
    let mut fields = Vec::new();
    let mut current = String::new();
    let mut in_quotes = false;
    for ch in line.chars() {
        if ch == '"' {
            in_quotes = !in_quotes;
        } else if ch == delimiter && !in_quotes {
            fields.push(current.trim().to_string());
            current = String::new();
        } else {
            current.push(ch);
        }
    }
    fields.push(current.trim().to_string());
    fields
}

